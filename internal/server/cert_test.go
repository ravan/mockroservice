func TestCertificateExpired(t *testing.T) {
	conf, err := config.GetConfig("")
	require.NoError(t, err)
	tempDir := t.TempDir()

	conf.Certificate.CertFile = filepath.Join(tempDir, "cert.pem")
	err = os.WriteFile(conf.Certificate.CertFile, []byte(ExpiredCert), 0644)
	require.NoError(t, err)

	conf.Certificate.KeyFile = filepath.Join(tempDir, "key.pem")
	err = os.WriteFile(conf.Certificate.KeyFile, []byte(ExpiredKey), 0644)
	require.NoError(t, err)

	conf.Certificate.Enabled = true
	err = validateCertAndKey(conf.Certificate.CertFile, conf.Certificate.KeyFile)
	require.Error(t, err)
}

